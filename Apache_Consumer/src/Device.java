
public class Device {
	
	private String client_ep;
	private String command;
	
	
	/**
	 * @return the command
	 */
	public String getCommand() {
		return command;
	}
	/**
	 * @param command the command to set
	 */
	public void setCommand(String command) {
		this.command = command;
	}
	/**
	 * @return the client_ep
	 */
	public String getClient_ep() {
		return client_ep;
	}
	/**
	 * @param client_ep the client_ep to set
	 */
	public void setClient_ep(String client_ep) {
		this.client_ep = client_ep;
	}
	
	
}
